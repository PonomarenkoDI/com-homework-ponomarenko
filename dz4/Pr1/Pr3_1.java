public class Pr3_1 {
    public static void main(String[] args) {
        double inf = Double.POSITIVE_INFINITY;
        System.out.println(inf);
        System.out.println(inf + 1);
        System.out.println(inf + 10);
        System.out.println(inf * -1);

        System.out.println();

        float inf1 = Float.POSITIVE_INFINITY;
        System.out.println(inf1);
        System.out.println(inf1 + 1);
        System.out.println(inf1 + 10);
        System.out.println(inf1 * -1);
    }
}
